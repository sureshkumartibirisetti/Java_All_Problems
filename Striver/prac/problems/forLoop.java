package Striver.prac.problems;

public class forLoop {
    public static void main(String[] args) {
        int i;
        for( i=0;i<=10;i++){
            System.out.print(i+" ");
        }
        System.out.println();
        System.out.println(i);
        int j;
        for( j=10;j<=20;j+=2){
            System.out.print(j+" ");
        }
        System.out.println();
        System.out.println(j);
    }
}
